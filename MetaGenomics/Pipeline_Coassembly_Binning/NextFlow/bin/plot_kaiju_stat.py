"""----------------------------------------------------------------------------
  Script Name: plot_kaiju_stat.py
  Description: Generates interactive density plots visualizing the distribution 
               of match lengths from Kaiju taxonomic classification results. 
               For each sample's Kaiju verbose output file, extracts the alignment 
               match length column (4th tab-separated field), computes read counts 
               per match length, and applies kernel density estimation (KDE) using 
               scipy.stats.gaussian_kde with user-defined smoothing parameter 
               (default: 0.3). Produces a single Plotly line plot overlaying 
               density curves for all input samples, enabling visual comparison 
               of match length distributions across samples. Supports gzipped 
               input files (.gz). Output is an interactive HTML file viewable 
               in any modern browser, with samples colored using a comprehensive 
               Plotly qualitative palette.
  Input files: 
    - One or more Kaiju verbose output files (tab-separated, with match length 
      in 4th column; typically *_kaiju.out or *_kaiju.out.gz format)
  Output files:
    - Interactive HTML file (default: match_length_kaiju_distribution.html) 
      containing Plotly line plot of KDE density curves per sample
  Created By:  Mathias Vandenbogaert
  Date:        2026-05-06
-------------------------------------------------------------------------------
"""

