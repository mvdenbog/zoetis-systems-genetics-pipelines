#!/usr/bin/env python3

"""----------------------------------------------------------------------------
  Script Name: bins_stat_to_multiqc.py
  Description: Aggregates bin statistics from multiple samples into MultiQC-
               compatible TSV tables for barplot visualization. For each input 
               bins stats file (with Quality categories), extracts: (1) bin 
               counts per quality category (High-quality, Medium-quality, 
               Low-quality, High-contamination), excluding the 'Not_binned' 
               entry; and (2) total bin size (sum of Size column) per quality 
               category, including 'Not_binned'. Sample names are derived by 
               removing the '_stat_and_quality.tsv' suffix from input filenames. 
               Outputs are transposed TSV tables with samples as rows and 
               quality categories as columns, ready for MultiQC generalstats 
               or bargraph modules.
  Input files: 
    - One or more bins stats TSV files (format: genome, Size, Quality columns; 
      typically generated by add_info_to_bin_stat.py)
  Output files:
    - bin_count_per_quality.tsv: counts of bins per Quality category per sample 
      (Not_binned excluded), samples as rows, categories as columns
    - bin_size_per_quality.tsv: sum of bin sizes per Quality category per sample 
      (Not_binned included), samples as rows, categories as columns
  Created By:  Mathias Vandenbogaert
  Date:        2026-05-06
-------------------------------------------------------------------------------
"""

# Metadata
__author__ = 'Mathias Vandenbogaert'
__version__ = '0.1'
__email__ = 'mathias.vandenbogaert@lizard.bio'
__status__ = 'dev'


from argparse import ArgumentParser, ArgumentDefaultsHelpFormatter
import logging
import pandas as pd


def parse_arguments():
    """Parse script arguments."""
    parser = ArgumentParser(description="Format bins stats to MultiQC TSV format for barplots.",
                            formatter_class=ArgumentDefaultsHelpFormatter)

    parser.add_argument('--bins_stats',  nargs='+', required=True, help="Bins stats with Quality category.")
    parser.add_argument("--out_bins_count", default='bin_count_per_quality.tsv' )
    parser.add_argument("--out_bins_size", default='bin_size_per_quality.tsv' )
    parser.add_argument("-v", "--verbose", help="increase output verbosity",
                        action="store_true")

    args = parser.parse_args()
    return args



def main():

    args = parse_arguments()

    if args.verbose:
        logging.basicConfig(format="%(levelname)s: %(message)s", level=logging.DEBUG)
        logging.info('Mode verbose ON')

    else:
        logging.basicConfig(format="%(levelname)s: %(message)s")
    
    
    stat_files = args.bins_stats

    df_list_count = []
    df_list_size = []

    logging.info(f'Parsing {len(stat_files)} bins stats files.')

    for stat_file in stat_files:
        sample_name = stat_file.replace('_stat_and_quality.tsv',"")

        df_ech = pd.read_csv(stat_file, sep='\t')

        #Get table with bin count per quality

        # remove Not_binned category when counting bins in different quality
        df_ech_count = df_ech.loc[~(df_ech['genome'] == "Not_binned")]

        df_ech_gr = df_ech_count.groupby(['Quality']).size().reset_index(name='counts')
        df_ech_gr = df_ech_gr.set_index("Quality")
 
        df_tr = df_ech_gr.transpose()

        df_tr['sample'] = sample_name
        df_tr = df_tr.set_index('sample')

        df_list_count.append(df_tr)

        #Get table with bin size per category (with unbinned size)
        df_ech_gr = df_ech.groupby(['Quality']).agg({"Size":sum}).reset_index()

        df_tr = df_ech_gr.set_index("Quality").transpose()

        df_tr['sample'] = sample_name
        df_tr = df_tr.set_index('sample')
        df_list_size.append(df_tr)

    df_count = pd.concat(df_list_count)
    df_size = pd.concat(df_list_size)



    logging.info(f'Writing {args.out_bins_count}')
    df_count.to_csv(args.out_bins_count, sep='\t')
    
    logging.info(f'Writing {args.out_bins_size}')
    df_size.to_csv(args.out_bins_size, sep='\t')

if __name__ == "__main__":
    main()