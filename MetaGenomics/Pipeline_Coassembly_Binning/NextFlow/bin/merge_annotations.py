#!/usr/bin/env python3

"""----------------------------------------------------------------------------
  Script Name: merge_annotations.py
  Description: Merges structural gene annotations from multiple GFF sources 
               (Prodigal CDS, barrnap rRNA, tRNAscan-SE tRNA) into unified 
               output files. Resolves overlaps by prioritizing RNA annotations 
               over CDS: any CDS overlapping an rRNA or tRNA feature is removed. 
               Generates standardized output files: (1) merged GFF3 with 
               hierarchical ID/Parent tagging for genes and features; (2) FFN 
               file with nucleotide sequences of all features (reverse-complemented 
               for minus strand); (3) FAA file with amino acid sequences for CDS 
               only (extracted from Prodigal output by matching feature IDs); 
               (4) Prokka-like summary report for MultiQC visualization. Contig 
               sequences are extracted from input FASTA using pyfastx for 
               efficient random access. Supports gzipped input GFF files.
  Input files: 
    - CDS GFF: Prodigal-generated GFF with CDS features (required)
    - tRNA GFF: tRNAscan-SE-generated GFF with tRNA features (required)
    - rRNA GFF: barrnap-generated GFF with rRNA features (required)
    - Contigs FASTA: nucleotide sequences of assembly contigs (required for 
      sequence extraction)
    - Prodigal FAA: amino acid sequences from Prodigal (required for CDS 
      protein output; matched by <contig>_<cds_number> ID pattern)
  Output files:
    - all_annotation.gff: merged GFF3 with gene/CDS/tRNA/rRNA features, 
      hierarchical IDs, sorted by coordinate
    - all_annotation.ffn: nucleotide sequences of all features (FASTA format)
    - all_annotation.faa: amino acid sequences of CDS features only (FASTA)
    - annotation_report.txt: Prokka-like summary (organism, contigs, bases, 
      feature counts) for MultiQC
  Created By:  Mathias Vandenbogaert
  Date:        2026-05-06
-------------------------------------------------------------------------------
"""

# Metadata
